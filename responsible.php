          <h2>OUR APPROACH</h2>
          <p>MARKETING OUR PRODUCTS RESPONSIBLY
As we don't want to contribute to excessive consumption or misuse, we have set standards and we comply with regional and national codes for beer and alcoholic advertising.
We only place commercial communication where at least 70% of the audience is over 18 years or in some countries the legal purchase age if that is higher than 18 years. We will only feature people who are at least 25 years old and act and appear their age. We will not suggest alcohol is the key to social or sexual success.
We define commercial communication as all activities carried out in order to market our brands (alcoholic and any non–alcoholic versions of alcoholic brands). This includes: all advertising, the brand name, product descriptor, packaging and labelling, digital and mobile phone communication, sponsorships, product placement, promotions (on & off–trade) and point of sale materials.</p>
          <h2>EMPLOYEES AS AMBASSADORS</h2>
          <p>We encourage our employees to be ambassadors for alcohol responsibility and to take personal responsibility for their own alcohol use.
Our company worldwide implements Cool@Work – a workplace alcohol information and prevention programme.
It aims to improve safety and health at work, ensure responsible alcohol consumption and create awareness, commitment and ambassadorship among employees.
Employees are divided into three groups and targeted information and support is delivered to each group:
Green: the majority who consume alcohol in moderation and are responsible drinkers.
Amber: Those at risk of developing alcohol problems because of their job activities or characteristics.
Red: Individuals showing performance or health problems because of their alcohol consumption.
Targeted communication and training is implemented annually by our global operating companies using tools and methods most appropriate to local market needs.</p>
          <h2>WORKING WITH THE INDUSTRY</h2>
          <p>Industry can be a valuable partner to encourage responsible drinking. The combined impact of brewers working together to address common issues is more powerful and in some cases has greater impact than companies acting alone.
Industry groups, governments, non–government organisations, consumer groups, police forces, legislators, retailers, bar and pub owners and community groups all have a valuable role to play in encouraging responsible consumption.
We work with brewers on a global (Worldwide Brewing Alliance), regional (e.g. Brewers of Europe) and market level on joint initiatives to promote responsible consumption. We also cooperate with the wider alcohol beverage industry on a global level (e.g. International Center for Alcohol Policies) and in many markets (e.g. Portman Group in the UK). </p>
          <h2>WORKING IN PARTNERSHIP </h2>
          <p>We work with others to encourage responsible attitudes to alcohol and to address alcohol related harm.
It has been recognised by the World Health Organisation that brewers can effectively contribute to reducing alcohol related harm.
We currently have partnerships with a variety of NGOs and third parties to address different areas of alcohol related harm.
Our commitment is that by 2015, all of our majority owned companies will have a partnership to help reduce alcohol related harm. </p>
          <h2>WHAT IS ALCOHOL? </h2>
          <p>Alcohol is created when fruits, vegetables or grains are fermented. That means using yeast or bacteria to chemically change sugar in the food into alcohol.
The chemical name for alcohol in drinks is ethanol. Ethanol is a colourless liquid that is lighter than water - one millilitre weighs around 0.8 grams. The amount of alcohol present in different fermented products varies greatly.
The percentage of alcohol in a typical serving of beer is around 5%; for wine it's about 12% and spirits approximately 35%.Alcoholic drinks also contain energy (calories) from alcohol (30 kilojoules per gram). The nutritional value of most alcoholic drinks is very low, because they contain little protein, fat or other nutrients. Some drinks like beer, do contain sugars and carbohydrates, B-vitamins, micronutrients and minerals.</p>


